    private int mapDelta;
    private int nextMapArrayPos;
    private int[][] nextMap;
    private boolean loadNextMap;
    private int oldMapSize;
    
    

    public MapRenderer(TileHandler tileManager) {
        map = new LinkedList<>();
        this.tileManager = tileManager;
    }
    
    public boolean getLoadNextMap() {
        return loadNextMap;
    }

    public void loadNewMap(int[][] nextMap) {
        this.nextMap = nextMap;
        loadNextMap = true;
        nextMapArrayPos = 0;
        mapDelta = nextMap.length - map.size();
        oldMapSize = map.size();
    }

    public void loadNewMapInstant(int[][] nextMap) {
        map.addAll(Arrays.asList(nextMap));
    }

    private void changeMap() {
        if (scroll >= tileManager.getTileSize()) {
            if (mapDelta > 0) { //Hvis det nye map er længere
                //Hvis vi er nået til den sidste række, så stop med at loade next map
                if (nextMapArrayPos == nextMap.length - 1) {
                    loadNextMap = false;
                    map.addFirst(nextMap[nextMapArrayPos]);
                    nextMapArrayPos = 0;
                } else {
                    //hvis  næste position er større end det gamle maps size, så er hele det gamle map fjernet, og der bliver derfor ikke fjernet fra mappet
                    if (nextMapArrayPos >= oldMapSize) {
                        map.addFirst(nextMap[nextMapArrayPos]);
                        nextMapArrayPos++;
                    } else {
                        //fjern sidste position og sæt en ny række ind fra det nye map
                        map.pollLast();
                        map.addFirst(nextMap[nextMapArrayPos]);
                        nextMapArrayPos++;
                    }
                }
            } else {
                if (mapDelta == 0) {
                    if (nextMapArrayPos == nextMap.length - 1) {
                        loadNextMap = false;
                        map.pollLast();
                        map.addFirst(nextMap[nextMapArrayPos]);
                        nextMapArrayPos = 0;
                    } else {
                        map.pollLast();
                        map.addFirst(nextMap[nextMapArrayPos]);
                        nextMapArrayPos++;
                    }
                }
                if (mapDelta < 0) {
                    if (nextMapArrayPos == nextMap.length - 1) {
                        loadNextMap = false;
                        map.pollLast();
                        map.addFirst(nextMap[nextMapArrayPos]);
                        nextMapArrayPos = 0;
                        for (int y = 0; y > mapDelta; y--) {
                            map.pollLast();
                        }
                    } else {
                        map.pollLast();
                        map.addFirst(nextMap[nextMapArrayPos]);
                        nextMapArrayPos++;
                    }
                }
            }
            scroll = 0;
        }
    }

    private void repeat() {
        if (scroll >= tileManager.getTileSize()) {
            int[] i = map.pollLast();
            map.addFirst(i);
            scroll = 0;
        }
    }

    public void render(GameContainer container, StateBasedGame game, Graphics g) throws SlickException {
        for (int y = 0; y < map.size(); y++) {
            for (int x = 0; x < map.get(y).length; x++) {
                tileManager.getTile(map.get(y)[x]).draw(x * tileManager.getTileSize(), y * tileManager.getTileSize() + scroll - tileManager.getTileSize());
            }
        }
    }

    public void verboseRender(GameContainer container, StateBasedGame game, Graphics g) throws SlickException {
        if (SettingsTool.getInstance().getPropertyAsBoolean("verbose")) {
            if (loadNextMap) {
                g.setColor(Color.white);
                g.fillRect(container.getWidth() - 210, 90, 200, 100);
                g.setColor(Color.red);
                g.drawString("Loading next map", container.getWidth() - 200, 100);
                g.setColor(Color.darkGray);
                g.drawString("MapDelta: " + mapDelta, container.getWidth() - 200, 120);
                g.drawString("NextMapArrayPos: " + nextMapArrayPos, container.getWidth() - 200, 140);
                g.drawString("OldMapSize: " + oldMapSize, container.getWidth() - 200, 160);
            }
        }
    }

